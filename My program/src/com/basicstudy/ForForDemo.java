package com.basicstudy;

public class ForForDemo {
    public static void main(String[] args) {
        for (int i=1;i<=3;i++){
            for (int j=1;j<=5;j++){
                    System.out.println("正在获取第"+i+"个班级的第"+j+"个同学");
            }
            System.out.println("---------------------------");
        }
    }
}
